# ---------------------------------------------------------------------------
# Storage
# ---------------------------------------------------------------------------

_DATA_ROOT   = Path(__file__).parent.parent / "data"
_REPORTS_DIR = _DATA_ROOT / "reports"
_ORDERS_DIR  = _DATA_ROOT / "orders"
_INV_DIR     = _DATA_ROOT / "inventory"


def _reports_dir(namespace: str) -> Path:
    d = _REPORTS_DIR / namespace
    d.mkdir(parents=True, exist_ok=True)
    return d


def _next_report_id(namespace: str) -> str:
    today = date.today().strftime("%Y%m%d")
    prefix = f"RPT-{today}-"
    d = _reports_dir(namespace)
    existing = [f.stem for f in d.glob(f"{prefix}*.pdf")]
    if existing:
        nums = [int(name.split("-")[-1]) for name in existing]
        seq = max(nums) + 1
    else:
        seq = 1
    return f"{prefix}{seq:03d}"


