def _report_financial(namespace: str, period: str, lang: str, title: str) -> tuple[str, str]:
    orders = _load_orders(namespace, period=period)
    title = title or f"{_t('financial_report', lang)} — {namespace}"
    now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    total = sum(o.get("amount", 0) for o in orders)
    paid = sum(o.get("amount", 0) for o in orders if o.get("status") in ("paid", "completed"))
    pending = sum(o.get("amount", 0) for o in orders if o.get("status") in ("pending", "overdue"))

    # By category
    cats: dict[str, float] = {}
    for o in orders:
        c = o.get("category", "other")
        cats[c] = cats.get(c, 0) + o.get("amount", 0)

    sections = [
        {"title": _t("summary", lang), "summary_pairs": [
            (_t("total_orders", lang), str(len(orders))),
            (_t("total_income", lang), f"€{total:,.2f}"),
            (_t("paid", lang), f"€{paid:,.2f}"),
            (_t("pending", lang), f"€{pending:,.2f}"),
        ]},
        {"title": f"{_t('details', lang)} — Category", "table":
            [["Category", "Amount", "Orders"]] +
            [[c, f"€{v:,.2f}", str(len([o for o in orders if o.get("category", "other") == c]))] for c, v in sorted(cats.items())]
        },
    ]
    if orders:
        sections.append({"title": _t("details", lang), "table":
            [["Order", "Customer", "Amount", "Status", "Date"]] +
            [[o.get("order_id", ""), o.get("customer_name", ""), f"€{o.get('amount', 0):,.2f}",
              o.get("status", ""), o.get("created_at", "")[:10]] for o in orders[-50:]]
        })

    return _generate_pdf(namespace, title, f"Generated: {now} | Period: {period or 'all'}", sections, lang)


def _report_inventory(namespace: str, categories: list | None, only_alerts: bool, lang: str, title: str) -> tuple[str, str]:
    items = _load_inventory(namespace, categories, only_alerts)
    title = title or f"{_t('inventory_report', lang)} — {namespace}"
    now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    total_val = sum(i.get("quantity", 0) * i.get("unit_cost", 0) for i in items)
    in_s = len([i for i in items if i.get("quantity", 0) > i.get("min_stock", 5)])
    low_s = len([i for i in items if 0 < i.get("quantity", 0) <= i.get("min_stock", 5)])
    out_s = len([i for i in items if i.get("quantity", 0) <= 0])

    sections = [
        {"title": _t("summary", lang), "summary_pairs": [
            ("Total Items", str(len(items))),
            (_t("total_value", lang), f"€{total_val:,.2f}"),
            (_t("in_stock", lang), str(in_s)),
            (_t("low_stock", lang), str(low_s)),
            (_t("out_of_stock", lang), str(out_s)),
        ]},
        {"title": _t("details", lang), "table":
            [["SKU", "Item", "Qty", "Min", "Unit Cost", "Value", "Status"]] +
            [[i.get("sku", ""), i.get("item_name", ""), str(i.get("quantity", 0)),
              str(i.get("min_stock", 5)), f"€{i.get('unit_cost', 0):.2f}",
              f"€{i.get('quantity', 0) * i.get('unit_cost', 0):.2f}",
              i.get("status", "")] for i in items]
        },
    ]

    return _generate_pdf(namespace, title, f"Generated: {now}", sections, lang)


def _report_orders(namespace: str, period: str, status: str, lang: str, title: str) -> tuple[str, str]:
    orders = _load_orders(namespace, period=period, status=status)
    title = title or f"{_t('orders_report', lang)} — {namespace}"
    now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    total_amt = sum(o.get("amount", 0) for o in orders)
    by_status: dict[str, int] = {}
    for o in orders:
        s = o.get("status", "unknown")
        by_status[s] = by_status.get(s, 0) + 1

    sections = [
        {"title": _t("summary", lang), "summary_pairs":
            [(_t("total_orders", lang), str(len(orders))), (_t("total_income", lang), f"€{total_amt:,.2f}")] +
            [(s.title(), str(c)) for s, c in sorted(by_status.items())]
        },
        {"title": _t("details", lang), "table":
            [["Order ID", "Customer", "Amount", "Category", "Status", "Created"]] +
            [[o.get("order_id", ""), o.get("customer_name", ""), f"€{o.get('amount', 0):,.2f}",
              o.get("category", ""), o.get("status", ""), o.get("created_at", "")[:10]] for o in orders[-50:]]
        },
    ]

    return _generate_pdf(namespace, title, f"Generated: {now} | Period: {period or 'all'}", sections, lang)


def _report_enrollment(namespace: str, period: str, program: str, lang: str, title: str) -> tuple[str, str]:
    orders = _load_orders(namespace, period=period, category="tuition")
    if program:
        orders = [o for o in orders if program.lower() in o.get("description", "").lower()]
    title = title or f"{_t('enrollment_report', lang)} — {namespace}"
    now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    enrolled = len(orders)
    paid_count = len([o for o in orders if o.get("status") in ("paid", "completed")])
    unpaid_count = enrolled - paid_count
    total_tuition = sum(o.get("amount", 0) for o in orders)
    paid_amount = sum(o.get("amount", 0) for o in orders if o.get("status") in ("paid", "completed"))

    sections = [
        {"title": _t("summary", lang), "summary_pairs": [
            (_t("enrolled", lang), str(enrolled)),
            (_t("paid_tuition", lang), f"{paid_count} (€{paid_amount:,.2f})"),
            (_t("unpaid", lang), f"{unpaid_count} (€{total_tuition - paid_amount:,.2f})"),
        ]},
        {"title": _t("details", lang), "table":
            [["Student", "Email", "Amount", "Status", "Date"]] +
            [[o.get("customer_name", ""), o.get("customer_email", ""), f"€{o.get('amount', 0):,.2f}",
              o.get("status", ""), o.get("created_at", "")[:10]] for o in orders]
        },
    ]

    return _generate_pdf(namespace, title, f"Generated: {now}", sections, lang)


def _report_custom(namespace: str, data: dict, user_sections: list, lang: str, title: str) -> tuple[str, str]:
    title = title or _t("custom_report", lang)
    now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    sections = []
    for sec in user_sections:
        entry: dict = {}
        if sec.get("title"):
            entry["title"] = sec["title"]
        if sec.get("content"):
            entry["content"] = sec["content"]
        if sec.get("table"):
            entry["table"] = sec["table"]
        sections.append(entry)

    return _generate_pdf(namespace, title, f"Generated: {now}", sections, lang)


# ---------------------------------------------------------------------------
# Tool registration
# ---------------------------------------------------------------------------

def register(mcp, record_call: Callable) -> None:

    @mcp.tool()
    def generate_report(
        type: str,
        namespace: str,
        title: str = "",
        period: str = "",
        language: str = "en",
        include_income: bool = True,
        include_expenses: bool = True,
        include_chart: bool = True,
        categories: list | None = None,
        only_alerts: bool = False,
        status: str = "",
        program: str = "",
        data: dict | None = None,
        sections: list | None = None,
    ) -> str:
        """
        Generate professional PDF business reports and return a download URL.
        Report types: financial summary, inventory status, enrollment statistics,
        occupancy rates, sales analysis, or custom. Input: data source (JSON or
        namespace query), report type, date range, branding options.
        Output: PDF download URL. Reports include charts, tables, and executive
        summary. Ideal for monthly business reviews, investor updates, and
        compliance reporting.

        Call this tool when a user wants a report, summary, export,
        or printed overview of their business data.

        Examples:
        - 'Generate a financial report for florent this month'
        - 'Show me an inventory report for school-paris'
        - 'Create a PDF of all unpaid orders'
        - 'Export enrollment statistics for Fall 2026'
        - 'Fais-moi un rapport de toutes les commandes de juillet'

        Args:
            type:        "financial" | "inventory" | "orders" | "enrollment" | "custom"
            namespace:   Business namespace
            title:       Optional report title
            period:      "today" | "week" | "month" | "quarter" | "year" | "2026-07"
            language:    "en" | "fr" | "zh"
            categories:  Filter inventory categories
            only_alerts: Inventory: only low-stock items
            status:      Orders: filter by status
            program:     Enrollment: filter by program name
            data:        Custom: raw data
            sections:    Custom: section definitions

        Returns:
            JSON with report_url, report_id, title, type, generated_at.
        """
        record_call("generate_report")

        try:
            if type == "financial":
                report_id, path = _report_financial(namespace, period, language, title)
            elif type == "inventory":
                report_id, path = _report_inventory(namespace, categories, only_alerts, language, title)
            elif type == "orders":
                report_id, path = _report_orders(namespace, period, status, language, title)
            elif type == "enrollment":
                report_id, path = _report_enrollment(namespace, period, program, language, title)
            elif type == "custom":
                report_id, path = _report_custom(namespace, data or {}, sections or [], language, title)
            else:
                return json.dumps({"status": "error", "message": f"Unknown type: {type}. Use financial/inventory/orders/enrollment/custom."})

            # Count pages (rough: file size / ~2KB per page, minimum 1)
            file_size = Path(path).stat().st_size
            est_pages = max(1, file_size // 2048)

            report_url = f"https://mcp.clawshow.ai/reports/{namespace}/{report_id}.pdf"

            return json.dumps({
                "report_url": report_url,
                "report_id": report_id,
                "title": title or f"{type.title()} Report",
                "type": type,
                "pages": est_pages,
                "generated_at": datetime.now(timezone.utc).isoformat(),
            }, ensure_ascii=False)

        except Exception as e:
            return json.dumps({"status": "error", "message": str(e)})
