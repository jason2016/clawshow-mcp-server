_ACCENT_COLOR = colors.HexColor("#FF385C")
_GRAY         = colors.HexColor("#6B7280")
_LIGHT_BG     = colors.HexColor("#F9FAFB")


def _make_styles():
    ss = getSampleStyleSheet()
    ss.add(ParagraphStyle("ReportTitle", parent=ss["Title"], fontSize=18, spaceAfter=6, textColor=colors.HexColor("#111827")))
    ss.add(ParagraphStyle("ReportSubtitle", parent=ss["Normal"], fontSize=10, textColor=_GRAY, spaceAfter=20))
    ss.add(ParagraphStyle("SectionHead", parent=ss["Heading2"], fontSize=13, textColor=colors.HexColor("#111827"), spaceBefore=16, spaceAfter=8))
    ss.add(ParagraphStyle("CellText", parent=ss["Normal"], fontSize=9, leading=12))
    ss.add(ParagraphStyle("FooterStyle", parent=ss["Normal"], fontSize=7, textColor=_GRAY, alignment=TA_CENTER))
    return ss


def _build_table(headers: list[str], rows: list[list], col_widths: list | None = None) -> Table:
    data = [headers] + rows
    t = Table(data, colWidths=col_widths, repeatRows=1)
    style = [
        ("BACKGROUND",  (0, 0), (-1, 0), _ACCENT_COLOR),
        ("TEXTCOLOR",   (0, 0), (-1, 0), colors.white),
        ("FONTSIZE",    (0, 0), (-1, 0), 9),
        ("FONTSIZE",    (0, 1), (-1, -1), 8),
        ("ALIGN",       (0, 0), (-1, -1), "LEFT"),
        ("VALIGN",      (0, 0), (-1, -1), "MIDDLE"),
        ("GRID",        (0, 0), (-1, -1), 0.5, colors.HexColor("#E5E7EB")),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 4),
        ("TOPPADDING",    (0, 0), (-1, -1), 4),
    ]
    # Alternate row colors
    for i in range(1, len(data)):
        if i % 2 == 0:
            style.append(("BACKGROUND", (0, i), (-1, i), _LIGHT_BG))
    t.setStyle(TableStyle(style))
    return t


def _build_summary_table(kv_pairs: list[tuple[str, str]]) -> Table:
    data = [[k, v] for k, v in kv_pairs]
    t = Table(data, colWidths=[200, 200])
    style = [
        ("FONTSIZE", (0, 0), (-1, -1), 10),
        ("TEXTCOLOR", (0, 0), (0, -1), _GRAY),
        ("FONTSIZE", (1, 0), (1, -1), 10),
        ("ALIGN", (1, 0), (1, -1), "RIGHT"),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 6),
        ("TOPPADDING", (0, 0), (-1, -1), 6),
        ("LINEBELOW", (0, 0), (-1, -2), 0.5, colors.HexColor("#E5E7EB")),
    ]
    t.setStyle(TableStyle(style))
    return t


def _footer_func(canvas, doc, text: str):
    canvas.saveState()
    canvas.setFont("Helvetica", 7)
    canvas.setFillColor(_GRAY)
    canvas.drawCentredString(A4[0] / 2, 15 * mm, text)
    canvas.drawRightString(A4[0] - 20 * mm, 15 * mm, f"Page {doc.page}")
    canvas.restoreState()


def _generate_pdf(namespace: str, title: str, subtitle: str, sections: list[dict], lang: str) -> tuple[str, str]:
    """Generate PDF, return (report_id, file_path)."""
    report_id = _next_report_id(namespace)
    out_path = _reports_dir(namespace) / f"{report_id}.pdf"
    ss = _make_styles()
    footer_text = _t("generated_by", lang)

    doc = SimpleDocTemplate(str(out_path), pagesize=A4,
                            topMargin=25 * mm, bottomMargin=25 * mm,
                            leftMargin=20 * mm, rightMargin=20 * mm)

    story: list = []
    story.append(Paragraph(title, ss["ReportTitle"]))
    story.append(Paragraph(subtitle, ss["ReportSubtitle"]))
    story.append(Spacer(1, 4 * mm))

    for sec in sections:
        if sec.get("title"):
            story.append(Paragraph(sec["title"], ss["SectionHead"]))
        if sec.get("content"):
            story.append(Paragraph(sec["content"], ss["Normal"]))
            story.append(Spacer(1, 3 * mm))
        if sec.get("summary_pairs"):
            story.append(_build_summary_table(sec["summary_pairs"]))
            story.append(Spacer(1, 4 * mm))
        if sec.get("table"):
            headers = sec["table"][0]
            rows = sec["table"][1:]
            story.append(_build_table(headers, rows, sec.get("col_widths")))
            story.append(Spacer(1, 4 * mm))

    doc.build(story, onFirstPage=lambda c, d: _footer_func(c, d, footer_text),
              onLaterPages=lambda c, d: _footer_func(c, d, footer_text))

    return report_id, str(out_path)


