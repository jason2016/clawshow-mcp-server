# Data loaders (read from orders/inventory JSON files)
# ---------------------------------------------------------------------------

def _load_orders(namespace: str, period: str = "", status: str = "", category: str = "") -> list[dict]:
    d = _ORDERS_DIR / namespace
    if not d.exists():
        return []
    orders = []
    for f in sorted(d.glob("ORD-*.json")):
        try:
            orders.append(json.loads(f.read_text(encoding="utf-8")))
        except Exception:
            pass

    if status and status != "all":
        orders = [o for o in orders if o.get("status") == status]
    if category:
        orders = [o for o in orders if o.get("category") == category]
    if period:
        orders = _filter_period(orders, period)
    return orders


def _load_inventory(namespace: str, categories: list | None = None, only_alerts: bool = False) -> list[dict]:
    d = _INV_DIR / namespace
    if not d.exists():
        return []
    items = []
    for f in sorted(d.glob("INV-*.json")):
        try:
            items.append(json.loads(f.read_text(encoding="utf-8")))
        except Exception:
            pass

    if categories:
        items = [i for i in items if i.get("category") in categories]
    if only_alerts:
        items = [i for i in items if i.get("quantity", 0) <= i.get("min_stock", 5)]
    return items


def _filter_period(items: list[dict], period: str) -> list[dict]:
    today = date.today()
    if period == "today":
        prefix = today.isoformat()
        return [i for i in items if i.get("created_at", "").startswith(prefix)]
    elif period == "week":
        start = (today - timedelta(days=today.weekday())).isoformat()
        return [i for i in items if i.get("created_at", "")[:10] >= start]
    elif period == "month":
        prefix = today.strftime("%Y-%m")
        return [i for i in items if i.get("created_at", "")[:7] == prefix]
    elif period == "quarter":
        q_month = ((today.month - 1) // 3) * 3 + 1
        prefix = f"{today.year}-{q_month:02d}"
        return [i for i in items if i.get("created_at", "")[:7] >= prefix]
    elif period == "year":
        prefix = str(today.year)
        return [i for i in items if i.get("created_at", "")[:4] == prefix]
    elif len(period) == 7:  # "2026-07"
        return [i for i in items if i.get("created_at", "")[:7] == period]
    return items


